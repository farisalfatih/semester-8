CREATE DATABASE IF NOT EXISTS uts_dw;

USE uts_dw;

CREATE TABLE IF NOT EXISTS dim_cabang (
    id_cabang INT PRIMARY KEY AUTO_INCREMENT,
    kode_cabang VARCHAR(10) NOT NULL UNIQUE,
    nama_cabang VARCHAR(50) NOT NULL,
    kota VARCHAR(30) NOT NULL,
    region VARCHAR(20) NOT NULL
);

CREATE TABLE IF NOT EXISTS dim_produk (
    id_produk INT PRIMARY KEY AUTO_INCREMENT,
    nama_produk VARCHAR(50) NOT NULL,
    kategori VARCHAR(30) NOT NULL,
    harga_satuan DECIMAL(10,2) NOT NULL
);

CREATE TABLE IF NOT EXISTS dim_pelanggan (
    id_pelanggan INT PRIMARY KEY AUTO_INCREMENT,
    nama_pelanggan VARCHAR(50) NOT NULL,
    jenis_kelamin ENUM('L','P') NOT NULL,
    golongan_umur VARCHAR(20) NOT NULL
);

CREATE TABLE IF NOT EXISTS dim_waktu (
    id_waktu INT PRIMARY KEY,
    tanggal DATE NOT NULL,
    hari VARCHAR(10) NOT NULL,
    bulan INT NOT NULL,
    tahun INT NOT NULL,
    kuartal INT NOT NULL
);

CREATE TABLE IF NOT EXISTS fact_penjualan (
    id_transaksi INT PRIMARY KEY AUTO_INCREMENT,
    id_cabang INT NOT NULL,
    id_produk INT NOT NULL,
    id_pelanggan INT NOT NULL,
    id_waktu INT NOT NULL,
    jumlah INT NOT NULL,
    total_harga DECIMAL(10,2) NOT NULL,

    FOREIGN KEY (id_cabang) REFERENCES dim_cabang(id_cabang),
    FOREIGN KEY (id_produk) REFERENCES dim_produk(id_produk),
    FOREIGN KEY (id_pelanggan) REFERENCES dim_pelanggan(id_pelanggan),
    FOREIGN KEY (id_waktu) REFERENCES dim_waktu(id_waktu)
);

